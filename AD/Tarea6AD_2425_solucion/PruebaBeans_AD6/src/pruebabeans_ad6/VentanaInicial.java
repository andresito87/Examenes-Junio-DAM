    /***************************************************************************
     * Método que rellena la tabla con los productos que nos devuelve ProductoBeans
     * a través de su método seleccionarFila, y los va listando añadiendo un número
     * de producto en la primera columna de la tabla.
     */
    private void actualizarTabla(){
        
        DefaultTableModel modelo = (DefaultTableModel) jTable_productos.getModel();
        modelo.setNumRows(0);
        int i=0;
        while(i<pb.numFilas()){
            pb.seleccionarFila(i);
            Object[] fila = {++i, 
                             pb.getReferencia(), 
                             pb.getNombre(), 
                             pb.getDescripcion(),
                             pb.getPrecio(), 
                             pb.getDescuento()};  // Datos según las columnas
            modelo.addRow(fila);
        }
    }
    /***************************************************************************
     * Método que borra todos los campos de texto del formulario de la ventana
     */
    private void limpiarCampos(){
        jTF_ref.setText("");
        jTF_nom.setText("");
        jTF_des.setText("");
        jTF_pre.setText("");
        jTF_descu.setText("");        
    }

