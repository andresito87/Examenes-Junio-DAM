    private void jButton_altaProductoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_altaProductoActionPerformed
        // TODO add your handling code here:
        if (jTF_ref.getText().isEmpty() || jTF_nom.getText().isEmpty() || jTF_des.getText().isEmpty() || jTF_pre.getText().isEmpty() || jTF_descu.getText().isEmpty())
            JOptionPane.showMessageDialog(null, "Todos los campos deben estar rellenos");
        else {
            try{
                Float precio=Float.parseFloat(jTF_pre.getText());
                Float descuento=Float.parseFloat(jTF_descu.getText());
                 
                pb.setReferencia(jTF_ref.getText());
                pb.setNombre(jTF_nom.getText());
                pb.setDescripcion(jTF_des.getText());
                pb.setPrecio(precio);
                pb.setDescuento(descuento);
                try {
                    if(!pb.existeProducto())
                        pb.addProducto();
                    else
                      JOptionPane.showMessageDialog(null, "Ya existe un producto con esa referencia");  
                        
                } 
                catch (ClassNotFoundException ex) {
                    Logger.getLogger(VentanaInicial.class.getName()).log(Level.SEVERE, null, ex);
                }  
                limpiarCampos();
            }
            catch(NumberFormatException e){
                JOptionPane.showMessageDialog(null, "Precio y descuento deben ser un valores númericos que pueden ser decimales");    
            }
            
        }
        
    }//GEN-LAST:event_jButton_altaProductoActionPerformed

    private void jButton_eliminarProductoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_eliminarProductoActionPerformed
        // TODO add your handling code here:
        if (jTF_ref.getText().isEmpty())
            JOptionPane.showMessageDialog(null, "Para borrar un producto debe indicar su referencia en el campo referencia");
        else {
            pb.setReferencia(jTF_ref.getText());
            try {
                if(pb.existeProducto())
                    pb.deleteProducto();
                else
                    JOptionPane.showMessageDialog(null, "No existe un producto con esa referencia");  
            } 
            catch (ClassNotFoundException ex) {
                Logger.getLogger(VentanaInicial.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        limpiarCampos();
    }//GEN-LAST:event_jButton_eliminarProductoActionPerformed

