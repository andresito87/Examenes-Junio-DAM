    /**
     * Captura el evento de modificación de base de datos, actualizando el contenido
     * de la tabla de productos y mostrando un mensaje informativo en función de cual
     * ha sido el evento producido.
     * @param ev 
     */
    @Override
    public void capturarBDModificada(BDModificadaEvent ev) {
        actualizarTabla();
        if (ev instanceof AddProductoEvent) 
            JOptionPane.showMessageDialog(null, "Se ha añadido un nuevo producto correctamente");
        if (ev instanceof DeleteProductoEvent) 
            JOptionPane.showMessageDialog(null, "Se ha eliminado el producto correctamente");
    }
