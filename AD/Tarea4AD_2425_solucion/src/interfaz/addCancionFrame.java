    /**
     * Método que comprueba que el parámetro recibido es un valor decimal
     * @param texto
     * @return 
     */
    private boolean esDecimal(String texto) {
        try {
            Double.parseDouble(texto);
            return true;
        } catch (NumberFormatException e) {
            return false;
        }
    }
    
    /**
     * Método que comprueba que el parámetro recibido es un valor entero
     * @param texto
     * @return 
     */
    private boolean esEntero(String texto) {
        try {
            Integer.parseInt(texto);
            return true;
        } catch (NumberFormatException e) {
            return false;
        }
    }
    /**
     * Método que actualiza el Combo con los autores que hay en la base de datos
     */
    static void actualizarComboAutores(){
        ObjectSet autores = obtenerTodosAutores();
        jCombo_autores.removeAllItems();
        while (autores.hasNext()){
            Autor a = (Autor)autores.next();
            jCombo_autores.addItem(a.getNombre());
        }
    }

