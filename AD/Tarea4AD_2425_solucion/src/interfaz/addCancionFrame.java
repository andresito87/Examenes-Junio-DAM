    private void jButton_añadirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_añadirActionPerformed
        // TODO add your handling code here:
         
        //validar que los datos son correctos
        String autorSeleccionado = (String) jCombo_autores.getSelectedItem();
        if (!(autorSeleccionado != null && !autorSeleccionado.isEmpty())){
            JOptionPane.showMessageDialog(null, "No es posible añadir una canción sin indicar su autor (añadir primero autor)");
        }
        else if (jTF_ide.getText().isEmpty() || jTF_tit.getText().isEmpty() || jTF_dur.getText().isEmpty() || jTF_año.getText().isEmpty() || jTF_gen.getText().isEmpty() ){
            JOptionPane.showMessageDialog(null, "Todos los campos deben estar rellenos");
        }
        else if (!esDecimal(jTF_dur.getText())) {
            JOptionPane.showMessageDialog(null, "La duración debe ser un valor numérico que puede ser decimal");
        }
        else if (!esEntero(jTF_año.getText())) {
            JOptionPane.showMessageDialog(null, "El año de creación debe ser un valor numérico entero");
        }
        else { //añadir nueva canción
               //todo el texto se almacena en mayúsculas para evitar confusiones
           JOptionPane.showMessageDialog(null, gestionBD.añadirCancion(jTF_ide.getText().toUpperCase(),
                                                                      jTF_tit.getText().toUpperCase(),
                                                                      Double.parseDouble(jTF_dur.getText()),
                                                                      Integer.parseInt(jTF_año.getText()),
                                                                      jTF_gen.getText().toUpperCase(),
                                                                      autorSeleccionado
                                                                      ));
            dispose();
        }
        
        
    }//GEN-LAST:event_jButton_añadirActionPerformed

